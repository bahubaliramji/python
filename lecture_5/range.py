for i in range(10): #range(stop)
    print(i)
print("loop 1 End")

for i in range(2,10): #range(start,stop)
    print(i)
print("loop 2 End")

for i in range(1,10,2): #range(start,stop,step)
    print(i)
print("loop 3 End")

# even
for i in range(2,101,2):
    print(i)
print("loop 4 End")

# odd
for i in range(1,100,2):
    print(i)
print("loop 4 End")



